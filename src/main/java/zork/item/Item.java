package zork.item;

public class Item {

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getUsage() {
        return usage;
    }

    public String getDescription() {
        return description;
    }

    protected String name;
    protected String type;
    protected String usage;
    protected String description;

}
